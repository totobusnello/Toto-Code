  /**
   * Save report to file
   */
  async saveReport(report: SimulationReport, outputPath: string, format: 'md' | 'json' | 'html'): Promise<string> {
    const fs = await import('fs/promises');
    const path = await import('path');

    let content: string;
    let extension: string;

    switch (format) {
      case 'md':
        content = await this.generateMarkdown(report);
        extension = 'md';
        break;
      case 'json':
        content = await this.generateJSON(report);
        extension = 'json';
        break;
      case 'html':
        content = await this.generateHTML(report);
        extension = 'html';
        break;
    }

    // Ensure output directory exists
    await fs.mkdir(outputPath, { recursive: true });

    // Generate filename
    const timestamp = new Date().toISOString().replace(/[:.]/g, '-');
    const filename = `${report.scenarioId}-${timestamp}.${extension}`;
    const fullPath = path.join(outputPath, filename);

    // Write file
    await fs.writeFile(fullPath, content, 'utf-8');

    return fullPath;
  }
